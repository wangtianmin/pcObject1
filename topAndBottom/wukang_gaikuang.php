						<p>徐汇区拥有2000余幢历史建筑，其中有不少是半个多世纪前的经典，这些遍布大街小巷、风格各异的历代建筑物是历史留给上海的珍贵遗产。对老房子的保护，不仅仅是修缮，还应该发掘丰富的人文价值和多样的社会价值。</p>
						<p>武康路位于上海市徐汇区东北部，北起华山路，南至淮海中路与兴国路交汇处，全线略呈弧形，全长1.17公里，是一条由北向南的单向行车道。原名福开森路，建于1907年，至1943年才更名为武康路，一公里见长的小马路，当时为上海法租界花园住宅区域的典型代表，后新建小型公寓和新式里弄房屋，被誉为上海著名的高级住宅区，整条马路环境幽静而高雅，是上海中心城区最具欧陆风情街区之一。2011年6月，文化部、国家文物局授予“上海武康路中国历史文化名街”荣誉称号，上海市文物局与徐汇区政府在393号内举行了上海武康路中国历史文化名街、名人旧居揭牌仪式。</p>
						<p>这里有典雅的英国乡村式别墅，有法国文艺复兴样式建筑，有细节雕饰的西班牙式洋房，有沪上至今少见的地中海式建筑，还有许多名人旧居、文物遗迹。这些历史建筑群通过富有特色的空间形式，充分展示了上海独特的海派文化，是宝贵的上海海派文化精髓的物质依托，是东西方文化交融的结晶和见证。
</p>
						<p>武康路上现有优秀历史建筑总计14处，保留历史建筑37处。武康路沿街商铺现已达到50余家，经营范围广泛。有像W.SANFORD Bespoke Garments的特色手工服饰制作，Wineshop的各国红酒供应，也有如Mentaigne Gallery的美术展览画廊和Seeker Café类的咖啡厅。其中，武康庭作为上海休闲场所的新地标，更是聚集了一批饶有特色的品质小店，如法国餐厅Franck Bistrot， Coffee Tree，RosaGallica花店以及狮语画廊等。
</p>
