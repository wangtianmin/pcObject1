			<p><img src="../../img/newsLetter/newsLetter12.jpg"/></p>
			<p>
	前有安迪·沃霍尔(Andy Warhol)，后有村上隆(Murakami Takashi)。商业元素和消费文化是他们的艺术创作中不可分离、乃至致力于探索的部分。要说模糊高雅艺术和通俗文化的界限，安迪·沃霍尔不是第一个，村上隆也绝对不是。但能把超通俗、超商业、超“幼稚”的视觉艺术带到凡尔赛宫展出，之后还正儿八经地发表《幼稚力宣言》的，村上隆可谓是独一无二。本次杜若云章画廊举办村上隆个展，展出多幅珍贵村上隆代表作品版画，包括他最富盛名的太阳花系列、骷髅头系列、MR. DOB系列，以及最新在东京举办个展时展出的《500罗汉》。其中《500罗汉》更是2015东京森美术馆村上隆五百罗汉展览官方出品，带艺术家亲笔签名全球限量30版。
			</p>
			<p><img src="../../img/newsLetter/newsLetter13.jpg"/></p>
			<p><span>【作者简介】</span></p>
			<p>
出生在战后东京的村上隆，正遇上经济飞速发展，消费主义爆棚的时代。他是少数将日本流行文化作为艺术创作主题传播至国际舞台的日本当代艺术家，而动画(Animation)－漫画(Comics)－电玩(Game)，也就是所谓的ACG，作为一种极富日本特色的文化，，从小就对村上隆产生了深刻影响，更是他作品的一大灵感来源。在发表《幼稚力宣言》之前，这位宣言狂人就曾在1996年发表过《超扁平宣言》： “将来的社会、风俗、艺术、文化，都会像日本一样，都变得极度平面……今天，日本电玩和卡通动画最能表现这种特质，而这些又在世界文化中具有强大的力量。”
			</p>
			<p><img src="../../img/newsLetter/newsLetter14.jpg"/></p>
			<p><span>【作品简介】</span></p>
			<p>
所谓“超扁平”(Super-flat)，就跟它的名字一样直白，即指以日本动漫为根源的纯二维(Two-dimensional)的画面。在好莱坞疯狂追求CG动画的逼真度的当下， 日本动漫仍坚持着二维动画的创作，可见日本文化对“平面”的情有独钟。这其中以浮世绘为代表的传统日本画的影响不可忽视，这也是村上隆曾经系统学习过的艺术形式。与西方艺术脉络截然不同的风格理念，也是村上隆在国际艺术舞台，特别是西方艺术界广受欢迎的原因之一。 
			</p>
			<p><img src="../../img/newsLetter/newsLetter15.jpg"/></p>
			<p>
	不具备任何写实性的画面充满了幻想感，却不似抽象艺术那样晦涩，视觉上来说，可谓是人人都能享受其中乐趣。另外，与安迪·沃霍尔大量运用现成的流行图案(Ready-made images)不同，村上隆代表性的视觉符号皆为他个人的原创，比如最为著名的太阳花。</p>
			<p><img src="../../img/newsLetter/newsLetter16.jpg"/></p>
			<p>还有骷髅头系列，常常被与死亡的话题联系在一起讨论，与可爱的造型和鲜艳的颜色形成戏剧性的对比。 
			</p>
			<p><img src="../../img/newsLetter/newsLetter17.jpg"/></p>
			<p>还有俨然已晋升为时尚宠儿的MR. DOB。 </p>
			<p><img src="../../img/newsLetter/newsLetter18.jpg"/></p>
			<p>
	如果说安迪·沃霍尔是以一种高冷的态度评价“量产”，那村上隆对同一图像元素的不同重复制造出令人眼花缭乱的视觉效果，以及神经质的、甚至是暗含癫狂的绚烂色彩与夸张造型，可谓是现代消费主义影响下人们精神状态的具像化体现，就像艺术家本人曾经说过“我的创作方式是要抓住人们的欲望”——而从这个角度来说，看似欢乐、“幼稚”的画面下，似乎又隐含着黑暗的色彩。另一方面，这种超扁平，超“幼稚”的风格让他在对艺术商业化的探索中也获得了巨大成功。他的作品《727》以超过一亿日圆高价卖出，让他成为日本作品拍卖价格最高的现代艺术。法国奢侈品大牌Louis Vuitton（LV）也频频与他合作，装饰有他设计图案的LV手袋，每只售价高达5000美元。 
			</p>
			<p><img src="../../img/newsLetter/newsLetter19.jpg"/></p>
			<p><span>村上隆从不乏追随者，他的作品中看似“幼稚”的吸引力，“幼稚”的追求，都是对当今流行文化最深刻的映射，就像他说的，“扁平”中有着巨大的表现力和强大的力量，令他的作品风靡全球。</span></p>
