			<p><span>【活动简介】</span></p>
			<p>2015年秋天， SCOP 推出了以人物为喻体的展览。 这个秋天,大自然一跃成为主角，通过摄影，我们探讨表现、结构和象征自然的画意方式；在这里，自然抑或是物理式或暗喻式的喻体，又抑或是单纯的景态。此次自然之旅中，陪伴我们的有 20 世纪著名美国摄影家哈里·卡拉汉以及我国 21 世纪摄影艺术家林然、罗永进、倪有鱼、沈学哲、史国威和吴舢锟。				</p>
			<p>
	当我们将“自然” 作为展览主题，关注的是对于自然世界充满浪漫色彩的爱，这片神奇的土地的美与力量所激发出的怀旧之情，以及自然作为人类社会的基石和晴雨表的功能。我们在面对自然时，内心会产生唏嘘敬畏之情，本次摄影展所集结的作品直接传达了这样的心声。当我们看到在清风中瑟瑟作响的树木；或在天空的映衬下，华丽而粗壮的树枝发着耀眼的光；亦或是在山脚下一片如地毯般铺开的厚厚的落叶；以及堤岸边的潺潺流水， 内心都会充满无限喜悦。观看是一种需要学习的技能，它需要练习的过程。为了让每件事物都能够被领会或看到，首先要将它们放进框架当中并展现出来。以此方式， 就如戴安· 阿勃丝 （ Diane Arbus）曾这样说过：“这世上存在无法被看到的事物， 除非我用相机把它拍摄下来。”（ There were things no one else could see unless I photographed them）此次“自然： 主观的景态”展览中，我们所希望展现的是当“ 真正” 的自然与艺术家视角的精确度和清晰度产生碰撞的时候，照片中的自然由于与人类经验的结合，能够拥有在视觉上、情感上以及理性上都产生震撼的威力， 同时培育人类对自身在自然的形成中扮演何种角色的更强烈的认知。
			</p>
			<p>
《自然：主观的景态》展出的70余幅作品体现了大量不同的风格、技巧、传统与创作手法，其中包括著名摄影师哈里·卡拉汉（Harry  Callahan，1912-99）从 20 世纪 40 年代早期至 70 年代中期所创作的革新性作品，罗永进和吴舢锟全新的大画幅作品；艺术家倪有鱼创作的优雅的拼贴作品；沈学哲和林然精美的风景摄影作品；以及史国威的一组精致的手工着色作品。
			</p>
			<p><img src="../../img/newsLetter/newsLetter21.jpg"/></p>
			<p><img src="../../img/newsLetter/newsLetter22.jpg"/></p>
			<p><span>【参展艺术家简介】</span></p>
			<p><span>哈里·卡拉汉</span></p>
			<p>
	1912 年生于美国底特律／1999 年逝于美国亚特兰大。作为 20 世纪下半叶最具影响力的美国摄影师之一， 哈里·卡拉汉自学成才。 哈里·卡拉汉拍摄黑白及彩色照片， 主题有妻子埃莉诺、 女儿芭芭拉， 也有自然和光的研究、芝加哥市中心的行人、 电话线、 普罗维登斯的建筑、 科德角的风景以及各地旅游的风光。</p>
			<p><span>林然</span></p>
			<p>
	1958 年生于重庆／现生活工作于成都。林然于 80 年代开始摄影创作。 他在摄影界的权威地位源于对古典摄影技法的研究与实践。 林然镜头中的景色孤独、 神秘， 荒芜的平原广阔、 令人着迷， 虽空无却又充满细节。</p>
			<p><span>吴舢锟</span></p>
			<p>
	1989 年生于广东珠海／现生活工作于北京。吴舢锟 2012 年获北京师范大学影视系学士学位， 2014 年获伦敦传媒学院摄影硕士学位。其作品《 一封来自远方的信》 获 2015 年度第七届三影堂摄影奖提名。 他主要探讨虚拟艺术与数字时代下人与环境的关系。</p>
			<p><span>倪有鱼</span></p>
			<p>
	1984 年生于上海／现生活工作于上海。倪有鱼 2007 年毕业于上海大学国画专业。 作为颇有名气的 80 后艺术家， 倪有鱼善于巧妙地将日常琐碎的事物运用到观念艺术中。 他的拼贴作品和他的绘画、 装置作品在内在手法上有某种一致性。
			</p>
			<p><span>罗永进</span></p>
			<p>
	1960年生于北京／现生活工作于上海。罗永进是中国最被国际认知和广泛展出的当代摄影师之一。 他最广为人知的是其对建筑和环境氛围的研究， 他的建筑作品起始于90年代末， 拍摄兰州地区和中国西部的农村家庭， 随后也纪录了广东、 浙江类似的发展， 通过地区性特征展示了逐步发展的时代品味。
			</p>
			<p><span>沈学哲</span></p>
			<p>
	1973年生于吉林延边／现生活工作于韩国。沈学哲自幼跟随父亲学习摄影及暗房工艺， 2000 年结识延边大学的一名韩国特聘教授，学习摄影理论知识， 并得以接触大量摄影大师的作品。 沈学哲出生成长于延边—中朝边境之城，《 边界之地豆满江》 系列中的豆满江承担着国家与社会意义的分界线角色。
			</p>
			<p><span>史国威</span></p>
			<p>
	1977 年生于河南洛阳／现生活工作于北京。史国威 2002 年毕业于清华大学美术学院摄影专业， 2006 年获德国多特蒙德高等专业学院摄影硕士学位。 从作品《祭》（ 2012） 开始， 人物题材逐渐从史国威的创作中淡出，取而代之的是平常无奇的、 抽象化的景物。 这个转变过程被艺术家看作是“ 从具象转向抽象， 由表象转入内涵”。
			</p>
