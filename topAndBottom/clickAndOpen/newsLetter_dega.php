			<p><span>【印象派大师埃德加·德加】</span></p>
			<p>
	作为印象派最著名的八大巨匠之一，艺术造诣于梵高、莫奈等大师齐名，是印象派中以传统精确素描与印象派色彩风格绝妙结合的画家，被称为“古典的印象主义”。</p>
			<p><img src="../../img/newsLetter/newsLetter27.jpg"/></p>
			<p><span>【德加在中国的首展】</span></p>
			<p>
	本次展览共计展出30件代表性原作，大部门来自塞尔维亚国家博物馆，这些作品均为德加1880年至1912年之间的代表作品，该博物馆由塞尔维亚王子于1910年至1920年捐赠并建立。此次德加作品为首次在中国展出，并由牛津大学艺术史教授JON WHITELEY担任策展人，JON是欧洲有名的印象派研究专家。</p>
			<p><img src="../../img/newsLetter/newsLetter28.jpg"/></p>
			<p><span>【光与色之“舞动”】</span></p>
			<p>
	德加表现芭蕾舞场景的作品甚多，为他赢得了“舞蹈女演员画家”的称誉。德加以她们的舞姿为媒介，刻意追寻光与色的表现。通过对舞女的造型描绘，表现出强烈的灯光反射效果。本次展览以舞女系列作品为主，全面地展示德加的光与色之舞的美丽世界。玛丽莲梦露垂帘爱慕的雕塑作品《14岁的小舞女》也将来到中国，此件雕塑作品创作于1878-1881年，是德加体量最大、工艺最复杂、精细的雕塑作品，也是德加生前唯一件曾展出过的雕塑作品。</p>
			<p><img src="../../img/newsLetter/newsLetter29.jpg"/></p>
			<p><span>【“舞动的艺术”】</span></p>
			<p>
	通过德加画作中的“芭蕾舞”“赛马”等运动元素，将现场、环境语境融汇成一场具有舞台动态氛围且唤起感官享受的视觉盛宴。本次展览还运用了最先进的虚拟技术VR，观众可以走进德加的画作中，不仅能感受德加作品中舞女们的优雅舞者，而且还能在虚拟空间中自己亲身体验拿起画笔创作舞动的艺术。同时，展览期间还组织多场芭蕾舞蹈表演，把现实中的舞蹈到架上的舞蹈有机结合，立体展示德加的“舞动的艺术”魅力!</p>
